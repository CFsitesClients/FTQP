<titre>Présentation du centre FTQP asbl.</titre>
<texte>
    Le centre de formation FTQP propose depuis le 26/03/1979 à des demandeurs d'emploi peu qualifiés de plus de 18 ans (détendeurs du CEB ou du CESI) deux formations qualifiantes dans le secteur de la construction : l'une en maçonnerie et l'autre en plafonnage.<br/><br/>

    Le stagiaire en formation devra être capable d'atteindre deux niveaux de compétences distincts :<br/>
    - Le niveau de base.<br/>
    - Le niveau de perfectionnement.<br/>
    Le stagiaire évolue à son rythme et dispose d'un délai maximum de 2 ans (2080 heures max) au centre à raison de 1040 heures de formation et 152 heures de stage en entreprise par an.<br/>
    Une mise à l'emploi au terme de la formation finalise le processus d'insertion.<br/>


    <h1>FTQP ALTERNANCE : EMPLOI FORMATION.</h1>

    Pour la 2ème année de formation, possibilité pour le(la) stagiaire âgé de 18 à 25 ans d'entrer dans un système en alternance organisé sur base de 1 jour  de formation et guidance et 4 jours en entreprise sous contrat RAC (Régime Apprentissage Construction du Fonds de Formation de la Construction FFC/FVB.
