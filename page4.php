    <p>Objectif de la  formation qualifiante du maçon et du plafonneur </p><br/>

    <p>Le stagiaire devra être capable d'atteindre deux niveaux de compétences distincts : 1) le niveau de base; 2) le niveau de perfectionnement.  Le stagiaire évolue à son rythme et dispose d'un délai maximum de 2 ans (2080 heures) au centre à raison de 1040 heures par an et 304 heures de stage en entreprise à raison de 152 heures par an.</p>
    <p>La formation se déroule du lundi au vendredi de 8h30 à 16h00.</p><br/>

    <p>Le stagiaire devra être capable d'intégrer les compétences associées aux activités clés du maçon et du plafonneur au travers de la réalisation de modules successifs en atelier (apprentissage pratique) et aux cours généraux (apprentissage théorique) sous la tutelle des formateurs.</p><br/>

    <p>Les stages en entreprise.<br/>
        Nous intégrons à nos formations des stages en entreprise (non rémunérés pour le 1er stage d'immersion) de 152 heures (4 semaines) par niveau de formation.<br/>
        - Le premier stage  consiste essentiellement en une première confrontation avec le milieu professionnel, il est centré sur le respect des règles du monde de l'entreprise et sur la mise en pratique sur chantier des apprentissages réalisés dans nos ateliers. Stage d'immersion.<br/>
        - Le deuxième stage a pour objectif d'être un tremplin vers l'emploi, une sorte de période d'essai avant la signature d'un contrat à durée indéterminée.  Stage d'achèvement.</p>
